  private void visitParameterList(NodeTraversal t, Node call,
      FunctionType functionType) {
    Iterator<Node> arguments = call.children().iterator();
    arguments.next(); // skip the function name

    Iterator<Node> parameters = functionType.getParameters().iterator();
    int ordinal = 0;
    Node parameter = null;
    Node argument = null;

    int maxArgs = functionType.getMaxArguments();

    while (arguments.hasNext() && (parameters.hasNext() || maxArgs == Integer.MAX_VALUE)) {
      if (parameters.hasNext()) {
        parameter = parameters.next();
      }
      // If there are more arguments than parameters, and this is a var_args
      // function (maxArgs == Integer.MAX_VALUE), then reuse the last parameter
      // (which should be the var_args parameter) for type checking.
      argument = arguments.next();
      ordinal++;

      validator.expectArgumentMatchesParameter(t, argument,
          getJSType(argument), getJSType(parameter), call, ordinal);
    }

    int numArgs = call.getChildCount() - 1;
    int minArgs = functionType.getMinArguments();
    if (minArgs > numArgs || maxArgs < numArgs) {
      report(t, call, WRONG_ARGUMENT_COUNT,
              validator.getReadableJSTypeName(call.getFirstChild(), false),
              String.valueOf(numArgs), String.valueOf(minArgs),
              maxArgs != Integer.MAX_VALUE ?
              " and no more than " + maxArgs + " argument(s)" : "");
    }
  }