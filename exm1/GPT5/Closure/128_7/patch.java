static boolean isSimpleNumber(String s) {
    int len = s.length();
    if (len == 0) {
      return false;
    }
    for (int index = 0; index < len; index++) {
      char c = s.charAt(index);
      if (c < '0' || c > '9') {
        return false;
      }
    }
    // Allow "0", but disallow other numbers with leading zeros like "01".
    return len == 1 || s.charAt(0) != '0';
  }